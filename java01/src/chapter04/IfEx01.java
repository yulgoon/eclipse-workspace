package chapter04;

public class IfEx01 {		// if 조건이 참일 때 if {수행문}, 거짓일 때 else {수행문} 출력

	public static void main(String[] args) {
		
		int age1 = 8;		// if 조건 01
		
		if ( age1 >= 8 ) {
			System.out.println("학교에 다닙니다.");
		}
		
		else {
			System.out.println("학교에 다니지 않습니다.");
		}
		
		
		int age2 = 7;		// if 조건 02
		
		if ( age2 >= 8 ) {
			System.out.println("학교에 다닙니다.");
			}
			
		else {
			System.out.println("학교에 다니지 않습니다.");
			}
			
	}
}
