package chapter02;

public class CharacterEx01 {

	public static void main(String[] args) {
		
		char ch1 = 'A'; //﻿ char의 값을 문자로 넣을 때는 'x' 형태로 입력 (작은 따옴표)
		System.out.println(ch1);
		System.out.println((int)ch1);	// 문자에 해당하는 정수값을 ASCII code로 출력
		
		char ch2 = 66;	// char의 값을 코드로 넣을 때는 숫자만 그대로 입력
		System.out.println(ch2);
		
		int ch3 = 67;
		System.out.println(ch3);
		System.out.println((char)ch3);

		
	}
	
}
